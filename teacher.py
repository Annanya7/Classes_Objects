class Teacher:
    def set_id(self,id):
        self.id=id
    def get_id(self):
        return self.id
    def set_name(self,name):
        self.name=name
    def get_name(self):
        return self.name
    def set_salary(self,sal):
        self.sal=sal
    def get_salary(self):
        return self.sal

    
